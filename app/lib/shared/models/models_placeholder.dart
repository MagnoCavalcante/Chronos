/// Modelos de Dados Compartilhados (Shared Models)
/// 
/// Define as estruturas e esquemas de dados compartilhados do aplicativo. 
/// Estes modelos mapeiam os dados que transitam entre diferentes telas
/// e garantem a tipagem forte do Dart.
class SharedModelPlaceholder {
  final String id;
  final String name;

  const SharedModelPlaceholder({
    required this.id,
    required this.name,
  });
}
